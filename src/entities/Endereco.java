package utilites;

public class Endereco {

	public int cep;
	public String rua;
	public String cidade;
	public String bairro;
	public int numero;
	public String estado;
	
	public Endereco(int cep, String rua, String cidade, String bairro, int numero, String estado) {
		this.cep = cep;
		this.rua = rua;
		this.cidade = cidade;
		this.bairro = bairro;
		this.numero = numero;
		this.estado = estado;
	}
	
}
